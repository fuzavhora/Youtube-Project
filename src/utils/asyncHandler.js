// Using promis

//Higher order function :-A higher order function is a function that takes another function as an argument or return another function.

//Here asynHandler takes requesthandler as an argument
const asyncHandler = (requestHandler) => {
  return (req, res, next) => {
    Promise.resolve(requestHandler(req, res, next)).catch((err) => next(err));
  };
};

export { asyncHandler };

